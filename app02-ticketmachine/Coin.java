/**
 * A coin class that has four set values.
 * 
 *
 * @Kamil Lesniewski
 * @version 21/10/2020
 */



public class Coin
    {
      
      // value of this coin
      private int value;
      
      /**
       * sets the value for each coin
       */
      public Coin(int value)
      {
           this.value = value;  
      }
      
      /**
       * returns the value of a coin
       */
      public int getValue()
      {
       return value;    
      }
    }
    
    
